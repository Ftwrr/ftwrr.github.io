function changeImage() {
  if (
    document.getElementById("johan").src ==
    "https://i.pinimg.com/originals/36/d3/a0/36d3a0fc51c40d18b0fb731ff36c80a5.gif"
  ) {
    document.getElementById("johan").src =
      "https://i.ytimg.com/vi/xzy_0m7jwRw/maxresdefault.jpg";
  } else {
    document.getElementById("johan").src =
      "https://i.pinimg.com/originals/36/d3/a0/36d3a0fc51c40d18b0fb731ff36c80a5.gif";
  }
}
